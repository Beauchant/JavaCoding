
public class Swapping {

	public static void main(String[] args) {
		// Swapping 
		int nbre1 = 10;
		int nbre2 = 15;
		
		System.out.println(nbre1);
		System.out.println(nbre2);
		System.out.println("-----------------");
		
		int temp = nbre1;
		nbre1 = nbre2;
		nbre2 = temp;
		
		System.out.println(nbre1);
		System.out.println(nbre2);
	}

}
